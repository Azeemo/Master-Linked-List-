class Solution{
    public:
    /* Should return data of middle node. If linked list is empty, then  -1*/
    int getMiddle(Node *head)
    {
        int total_val = 0;
        Node * ptr = head;
        while(ptr!=NULL){
            ptr=ptr->next;
            total_val++;
        }
        int num ;
    if(total_val%2==0){
        num = total_val/2 +1;
    }
    else{
        
        num = (total_val+1)/2;
    }
        ptr = head;
        for(int i=2;i<=num;i++){
            ptr=ptr->next;
        }
        int vals = ptr->data;
        return vals;
    }
};


